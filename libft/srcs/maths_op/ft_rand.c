int		ft_rand(int min, int max)
{
	int n;
	int	rng;

	n = 10 * (int)&rng;
	n = (n < 0) ? -n : n;
	rng = 0;
	while ((n /= 10) >= 10)
		rng += (n % 10);
	rng = (rng % (max - min + 1)) + min;
	return (rng);
}
