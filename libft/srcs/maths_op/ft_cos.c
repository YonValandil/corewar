#include "maths_op.h"

double	ft_cos(double x)
{
	double	y;
	double	s;
	int		i;

	y = 1.0;
	s = -1.0;
	i = 2;
	while (i < 100)
	{
		y += s * (ft_dpow(x, i) / ft_dfact(i));
		s *= -1;
		i += 2;
	}
	return (y);
}
