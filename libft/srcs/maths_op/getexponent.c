int		ft_getexp(double x)
{
	int		i;
	double	y;

	y = x > 0 ? x : -x;
	i = 0;
	if (y > 1.0)
		while (y >= 10.0)
		{
			y /= 10.0;
			i++;
		}
	else
		while (y < 1.0)
		{
			y *= 10.0;
			i--;
		}
	return (i);
}
