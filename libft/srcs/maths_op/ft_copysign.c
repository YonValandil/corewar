double	ft_copysign(double x, double y)
{
	if ((x < 0.0 && y > 0.0) || (x > 0.0 && y < 0.0))
		return (-x);
	return (x);
}

float	ft_copysignf(float x, float y)
{
	if ((x < 0.0f && y > 0.0f) || (x > 0.0f && y < 0.0f))
		return (-x);
	return (x);
}
