#include "output_mgmt.h"

void	ft_print_bits(unsigned int x, uint8_t bits)
{
	int8_t n[3];

	n[0] = 8 * sizeof(x) - 1;
	n[1] = bits - 1;
	n[2] = (n[0] > n[1] ? n[0] : n[1]);
	if (n[2] == n[0])
		while (!(x & (1 << n[2])) && n[2] > (n[1]))
			n[2]--;
	while (n[2] >= 0)
		(x & (1 << n[2]--)) ? ft_putchar('1') : ft_putchar('0');
	ft_putchar('\n');
}
